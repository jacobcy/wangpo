var util = {
  /**
   * Converts date string of yymmdd to Date object.
   * @param {String} text date string of yymmdd format, such as 860214
   * @returns {Date} null if fails.
   */
  parseShortDate: function(text) {
    if (text.length !== 6) {
      return null;
    }
    var year = parseInt(text.substr(0, 2));
    if (isNaN(year)) {
      return null;
    }
    if (year <= 40) {
      year += 2000;
    }
    var month = text.substr(2, 2);
    var day = text.substr(4, 2);
    var date = new Date(year.toString() + '/' + month + '/' + day);
    return isFinite(date) ? date : null;
  },

  /**
   * 将日期转化为YYYY年X月X日格式
   */
  toBirthdayString: function(date) {
    return date.getFullYear() + '年' + (date.getMonth() + 1) + '月' + date.getDate() + '日';
  },

  /**
   * 通过电话区号获取对应的城市名称
   */
  areaCodeToCity: function(codeString) {
    return CityMap[codeString];
  }
};

module.exports = util;

